# Arithmetic
print(4+5)
print(8-6)
print(9/3)
print(5*2)
print(5//2)
print(5%2)
print(2**2)

# assignment
x = 5
x+=5
print(x)

# Comparison
print(5==5)

# Logical
a = True
b = False
print(a and b)

# identity
print(a is not b)

# membership
lst = [3, 4, 5, 6, 7]
print(5 in lst)

# bitwise
a = 0
b = 1
print(a&b)
print(a|b)